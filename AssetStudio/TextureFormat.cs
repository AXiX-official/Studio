    public static class TextureFormatConverter
    {
        public static TextureFormat ConvertToOld(NewTextureFormat newStatus)
        {
            if (Enum.TryParse(newStatus.ToString(), out TextureFormat oldStatus))
            {
                return oldStatus;
            }
            throw new ArgumentException($"cant convert {newStatus} to {nameof(TextureFormat)}");
        }
    }
    
